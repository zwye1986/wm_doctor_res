package com.pinde.sci.form.test;

public class TestResultForm {
	
	private String paperName;
	private String passFlag;
	private String testTime;
	private String testTimeTwo;
	private String paperTypeId;
	public String getPaperName() {
		return paperName;
	}
	public void setPaperName(String paperName) {
		this.paperName = paperName;
	}
	
	public String getPassFlag() {
		return passFlag;
	}
	public void setPassFlag(String passFlag) {
		this.passFlag = passFlag;
	}
	public String getTestTime() {
		return testTime;
	}
	public void setTestTime(String testTime) {
		this.testTime = testTime;
	}
	public String getPaperTypeId() {
		return paperTypeId;
	}
	public void setPaperTypeId(String paperTypeId) {
		this.paperTypeId = paperTypeId;
	}
	public String getTestTimeTwo() {
		return testTimeTwo;
	}
	public void setTestTimeTwo(String testTimeTwo) {
		this.testTimeTwo = testTimeTwo;
	}
	
	
}
