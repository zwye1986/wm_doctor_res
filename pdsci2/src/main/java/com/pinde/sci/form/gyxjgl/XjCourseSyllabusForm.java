package com.pinde.sci.form.gyxjgl;




public class XjCourseSyllabusForm {

    //周次
    private String weekTimes;
    //月/日
    private String  monthOrDay;
    //星期
    private String  week;

    //时数
    private String  hours;

    //节次
    private String  festivals;

    //讲授内容（理论课或实验课）
    private String  teachingContent;

    //授课人
    private String  teachingman;

    public String getWeekTimes() {
        return weekTimes;
    }

    public void setWeekTimes(String weekTimes) {
        this.weekTimes = weekTimes;
    }

    public String getMonthOrDay() {
        return monthOrDay;
    }

    public void setMonthOrDay(String monthOrDay) {
        this.monthOrDay = monthOrDay;
    }

    public String getWeek() {
        return week;
    }

    public void setWeek(String week) {
        this.week = week;
    }

    public String getHours() {
        return hours;
    }

    public void setHours(String hours) {
        this.hours = hours;
    }

    public String getFestivals() {
        return festivals;
    }

    public void setFestivals(String festivals) {
        this.festivals = festivals;
    }

    public String getTeachingContent() {
        return teachingContent;
    }

    public void setTeachingContent(String teachingContent) {
        this.teachingContent = teachingContent;
    }

    public String getTeachingman() {
        return teachingman;
    }

    public void setTeachingman(String teachingman) {
        this.teachingman = teachingman;
    }







}
