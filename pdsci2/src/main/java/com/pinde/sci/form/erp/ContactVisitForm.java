package com.pinde.sci.form.erp;

public class ContactVisitForm {
    
	private String customerLinkManFlow;//客户联系人流水号
	
	private String customerLinkMan;//客户联系人名称
	
	private String visitDate;//回访日期
	
	private String isSolved;//是否解决问题
	
	private String visitContent;//回访内容
	
	private String visitor;//回访人



	public String getCustomerLinkManFlow() {
		return customerLinkManFlow;
	}

	public void setCustomerLinkManFlow(String customerLinkManFlow) {
		this.customerLinkManFlow = customerLinkManFlow;
	}

	public String getCustomerLinkMan() {
		return customerLinkMan;
	}

	public void setCustomerLinkMan(String customerLinkMan) {
		this.customerLinkMan = customerLinkMan;
	}

	public String getVisitDate() {
		return visitDate;
	}

	public void setVisitDate(String visitDate) {
		this.visitDate = visitDate;
	}

	public String getIsSolved() {
		return isSolved;
	}

	public void setIsSolved(String isSolved) {
		this.isSolved = isSolved;
	}

	public String getVisitContent() {
		return visitContent;
	}

	public void setVisitContent(String visitContent) {
		this.visitContent = visitContent;
	}

	public String getVisitor() {
		return visitor;
	}

	public void setVisitor(String visitor) {
		this.visitor = visitor;
	}
	
	
	
}
