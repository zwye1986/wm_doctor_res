package com.pinde.sci.form.res;

public class CaseRegistryForm {
	private String patientName;
	private String hospitalNumbers;
	private String diagnoseType;
	private String deptAppraise;
	private String deptHeadAutograth;
	private String auditResult;
	public String getPatientName() {
		return patientName;
	}
	public void setPatientName(String patientName) {
		this.patientName = patientName;
	}
	public String getHospitalNumbers() {
		return hospitalNumbers;
	}
	public void setHospitalNumbers(String hospitalNumbers) {
		this.hospitalNumbers = hospitalNumbers;
	}
	public String getDiagnoseType() {
		return diagnoseType;
	}
	public void setDiagnoseType(String diagnoseType) {
		this.diagnoseType = diagnoseType;
	}
	public String getAuditResult() {
		return auditResult;
	}
	public void setAuditResult(String auditResult) {
		this.auditResult = auditResult;
	}
	public String getDeptAppraise() {
		return deptAppraise;
	}
	public void setDeptAppraise(String deptAppraise) {
		this.deptAppraise = deptAppraise;
	}
	public String getDeptHeadAutograth() {
		return deptHeadAutograth;
	}
	public void setDeptHeadAutograth(String deptHeadAutograth) {
		this.deptHeadAutograth = deptHeadAutograth;
	}
}
