package com.pinde.sci.form.erp;

public class ContactOrderTrackForm {
	
	private String trackDate;
	
	private String trackContent;
	
	private String recordUserFlow;
	
	private String recordUserName;
	
	private String recordDate;

	public String getTrackDate() {
		return trackDate;
	}

	public void setTrackDate(String trackDate) {
		this.trackDate = trackDate;
	}

	public String getTrackContent() {
		return trackContent;
	}

	public void setTrackContent(String trackContent) {
		this.trackContent = trackContent;
	}

	public String getRecordUserFlow() {
		return recordUserFlow;
	}

	public void setRecordUserFlow(String recordUserFlow) {
		this.recordUserFlow = recordUserFlow;
	}

	public String getRecordUserName() {
		return recordUserName;
	}

	public void setRecordUserName(String recordUserName) {
		this.recordUserName = recordUserName;
	}

	public String getRecordDate() {
		return recordDate;
	}

	public void setRecordDate(String recordDate) {
		this.recordDate = recordDate;
	}
	
	

}
