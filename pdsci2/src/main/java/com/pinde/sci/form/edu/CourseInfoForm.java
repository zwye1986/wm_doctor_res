package com.pinde.sci.form.edu;

public class CourseInfoForm {
    
	private String highScoreCount;
	
	private String middleScoreCount;
	
	private String lowScoreCount;
	
	private String praiseCount;
	
	private String collectionCount;
	
	private String answeredCount;
	
	private String unansweredCount;
	
	private String allQuestionCount;
	
	private String leaveMessageCount;

	public String getHighScoreCount() {
		return highScoreCount;
	}

	public void setHighScoreCount(String highScoreCount) {
		this.highScoreCount = highScoreCount;
	}

	public String getMiddleScoreCount() {
		return middleScoreCount;
	}

	public void setMiddleScoreCount(String middleScoreCount) {
		this.middleScoreCount = middleScoreCount;
	}

	public String getLowScoreCount() {
		return lowScoreCount;
	}

	public void setLowScoreCount(String lowScoreCount) {
		this.lowScoreCount = lowScoreCount;
	}

	public String getPraiseCount() {
		return praiseCount;
	}

	public void setPraiseCount(String praiseCount) {
		this.praiseCount = praiseCount;
	}

	public String getCollectionCount() {
		return collectionCount;
	}

	public void setCollectionCount(String collectionCount) {
		this.collectionCount = collectionCount;
	}

	public String getAnsweredCount() {
		return answeredCount;
	}

	public void setAnsweredCount(String answeredCount) {
		this.answeredCount = answeredCount;
	}

	public String getUnansweredCount() {
		return unansweredCount;
	}

	public void setUnansweredCount(String unansweredCount) {
		this.unansweredCount = unansweredCount;
	}

	public String getAllQuestionCount() {
		return allQuestionCount;
	}

	public void setAllQuestionCount(String allQuestionCount) {
		this.allQuestionCount = allQuestionCount;
	}

	public String getLeaveMessageCount() {
		return leaveMessageCount;
	}

	public void setLeaveMessageCount(String leaveMessageCount) {
		this.leaveMessageCount = leaveMessageCount;
	}
	
	
}
