package com.pinde.sci.form.erp;

public class ContractTimeForm {

	private String signDateStart;
	
	private String signDateEnd;
	
	private String maintainDueDateStart;
	
	private String maintainDueDateEnd;
	
	private String contractDueDateStart;
	
	private String contractDueDateEnd;

	public String getSignDateStart() {
		return signDateStart;
	}

	public void setSignDateStart(String signDateStart) {
		this.signDateStart = signDateStart;
	}

	public String getSignDateEnd() {
		return signDateEnd;
	}

	public void setSignDateEnd(String signDateEnd) {
		this.signDateEnd = signDateEnd;
	}

	public String getMaintainDueDateStart() {
		return maintainDueDateStart;
	}

	public void setMaintainDueDateStart(String maintainDueDateStart) {
		this.maintainDueDateStart = maintainDueDateStart;
	}

	public String getMaintainDueDateEnd() {
		return maintainDueDateEnd;
	}

	public void setMaintainDueDateEnd(String maintainDueDateEnd) {
		this.maintainDueDateEnd = maintainDueDateEnd;
	}

	public String getContractDueDateStart() {
		return contractDueDateStart;
	}

	public void setContractDueDateStart(String contractDueDateStart) {
		this.contractDueDateStart = contractDueDateStart;
	}

	public String getContractDueDateEnd() {
		return contractDueDateEnd;
	}

	public void setContractDueDateEnd(String contractDueDateEnd) {
		this.contractDueDateEnd = contractDueDateEnd;
	}
	
	
}
